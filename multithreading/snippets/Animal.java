
public class Animal {
    
    int age;
    String name;

    public Animal(int age, String name) {
        this.age = age;
        this.name = name;
    }

    public void eat() {
        System.out.println(name + " is eating");
    }

    public void dance()

    {
        System.out.println(name + " is dancing");
    }


}

